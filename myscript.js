
function block() {
    var element = document.getElementById('oculto');
  
    // Verifica o estado atual do display
    var currentDisplay = element.style.display;
  
    // Altera o display com base no estado atual
    if (currentDisplay === 'none' || currentDisplay === '') {
      element.style.display = 'block';
    } else {
      element.style.display = 'none';
    }
  }


